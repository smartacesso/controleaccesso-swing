package com.protreino.services.to;

public class AttachedTO {

	private String nomeDevice;
	private String localDevice;
	private String idDevice;
	
	public AttachedTO() {}

	public String getNomeDevice() {
		return nomeDevice;
	}
	public void setNomeDevice(String nomeDevice) {
		this.nomeDevice = nomeDevice;
	}
	public String getLocalDevice() {
		return localDevice;
	}
	public void setLocalDevice(String localDevice) {
		this.localDevice = localDevice;
	}
	public String getIdDevice() {
		return idDevice;
	}
	public void setIdDevice(String idDevice) {
		this.idDevice = idDevice;
	}
}
